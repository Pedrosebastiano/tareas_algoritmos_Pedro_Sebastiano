def insertion_sort(vector):
    n = len(vector)
    for i in range(n-2, -1, -1):
        temp = vector[i]
        j = i + 1
        while j < n and temp > vector[j]:
            vector[j-1] = vector[j]
            j += 1
        vector[j-1] = temp

def main():
    numbers = [63, 623, 819, 818, 892, 2, 25, 367, 88, 368, 90]
    insertion_sort(numbers)
    print(numbers)

main()

