def selection_sort(vector):
    n = len(vector)
    for i in range(n-1, 0, -1):
        max_idx = i
        for j in range(i):
            if vector[max_idx] < vector[j]:
                max_idx = j
        vector[i], vector[max_idx] = vector[max_idx], vector[i]

def main():
    numbers = [63, 623, 819, 818, 892, 2, 25, 367, 88, 368, 90]
    selection_sort(numbers)
    print(numbers)

main()

