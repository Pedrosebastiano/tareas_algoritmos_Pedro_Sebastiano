def bubble_sort(vector):
    n = len(vector)
    for i in range(n-1, 0, -1): 
        for j in range(i):
            if vector[j] > vector[j+1]: 
                vector[j], vector[j+1] = vector[j+1], vector[j]

def main():
    numbers = [63, 623, 819, 818, 892, 2, 25, 367, 88, 368, 90]
    bubble_sort(numbers)
    print(numbers)

main()

